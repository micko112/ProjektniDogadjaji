    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed
        try {
            // TODO add your handling code here:
            
            String nazivBaze = jTextFieldNazivBaze.getText();
            String username = jTextFieldUSERNAME.getText();
            String password = String.valueOf(jPasswordField.getPassword());

            if (nazivBaze.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Naziv baze ne sme biti prazan!");
                return;
            }

            Properties prop = new Properties();
            FileOutputStream out = new FileOutputStream("dbconfiguration.properties");

            prop.setProperty("url", "jdbc:mysql://localhost:3306/" + nazivBaze);
            prop.setProperty("username", username);
            prop.setProperty("password", password);

            prop.store(out, null);

            JOptionPane.showMessageDialog(this, "Uspesno sacuvana konfiguracija.");
            this.dispose();
        } catch (IOException ex) {
            java.util.logging.Logger.getLogger(Konfiguracija.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }

        

    }//GEN-LAST:event_jButtonSaveActionPerformed

