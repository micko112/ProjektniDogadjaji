    private void jButtonPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPretraziActionPerformed
        String kriterijumPretrage = jTextFieldPretraga.getText().trim();
    
    // šablon za pretragu
    Gost filter = new Gost();
    filter.setPrezime(kriterijumPretrage); // Pretraživaćemo po prezimenu
    
    try {
        ArrayList<Gost> pronadjeni = CController.getInstance().pronadjiGoste(filter);
        
        TableModelGost model = (TableModelGost) jTableGosti.getModel();
        model.setListaGostiju(pronadjeni); 
        
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Greška pri pretrazi: " + ex.getMessage(), "Greška", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButtonPretraziActionPerformed

