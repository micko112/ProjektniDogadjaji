    private void jButtonObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonObrisiActionPerformed
        int selektovanRed = jTableGosti.getSelectedRow();
        if (selektovanRed == -1) {
            JOptionPane.showMessageDialog(this, "Morate izabrati red za brisanje!", "Greška", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int odgovor = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da želite da obrišete izabranog gosta?", "Potvrda brisanja", JOptionPane.YES_NO_OPTION);
        if (odgovor == JOptionPane.YES_OPTION) {
            try {
                TableModelGost model = (TableModelGost) jTableGosti.getModel();
                Gost gostZaBrisanje = model.getGost(selektovanRed);

                CController.getInstance().obrisiGosta(gostZaBrisanje);
                JOptionPane.showMessageDialog(this, "Gost je uspešno obrisan.", "Uspeh", JOptionPane.INFORMATION_MESSAGE);

                osveziPrikaz();

            } catch (Exception ex) {
                if (ex.getMessage().contains("foreign key constraint")) {
                    JOptionPane.showMessageDialog(this, "Ne možete obrisati gosta jer je povezan sa postojećim događajima (potvrdama)!", "Greška", JOptionPane.ERROR_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Došlo je do greške pri brisanju: " + ex.getMessage(), "Greška", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_jButtonObrisiActionPerformed

