    private void jButtonObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonObrisiActionPerformed
        int selektovanRed = jTableLokacije.getSelectedRow();
        if (selektovanRed == -1) {
            JOptionPane.showMessageDialog(this, "Morate izabrati red za brisanje!", "Greška", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        int odgovor = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da želite da obrišete izabranu lokaciju?", "Potvrda brisanja", JOptionPane.YES_NO_OPTION);
        if (odgovor == JOptionPane.YES_OPTION) {
            try {
                TableModelLokacija model = (TableModelLokacija) jTableLokacije.getModel();
                Lokacija lokacijaZaBrisanje = model.getLokacija(selektovanRed);
                
                CController.getInstance().obrisiLokaciju(lokacijaZaBrisanje);
                JOptionPane.showMessageDialog(this, "Lokacija je uspešno obrisana.", "Uspeh", JOptionPane.INFORMATION_MESSAGE);
                osveziPrikaz();
                
            } catch (Exception ex) {
                if (ex.getMessage().contains("foreign key constraint")) { 
                     JOptionPane.showMessageDialog(this, "Ne možete obrisati lokaciju jer je povezana sa postojećim događajima!", "Greška", JOptionPane.ERROR_MESSAGE);
                } else {
                     JOptionPane.showMessageDialog(this, "Došlo je do greške pri brisanju: " + ex.getMessage(), "Greška", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_jButtonObrisiActionPerformed

