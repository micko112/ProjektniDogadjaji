    private void jButtonIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIzmeniActionPerformed
        int selektovanRed = jTableLokacije.getSelectedRow();
        if (selektovanRed == -1) {
            JOptionPane.showMessageDialog(this, "Morate izabrati red za izmenu!", "Greška", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        TableModelLokacija model = (TableModelLokacija) jTableLokacije.getModel();
        Lokacija lokacijaZaIzmenu = model.getLokacija(selektovanRed);

        new LokacijuKreirajDialog(this, lokacijaZaIzmenu).setVisible(true);
    }//GEN-LAST:event_jButtonIzmeniActionPerformed

