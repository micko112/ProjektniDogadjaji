    private void pripremiFormu() {
        try {
            jComboBoxIzvodjac.removeAllItems();
            ArrayList<Izvodjac> izvodjaci = CController.getInstance().vratiSveIzvodjace();
            for (Izvodjac i : izvodjaci) {
                jComboBoxIzvodjac.addItem(i);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Greška pri učitavanju izvođača!", "Greška", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void popuniPolja() {
        jComboBoxIzvodjac.setSelectedItem(angazman.getIzvodjac());
        jTextFieldTrajanje.setText(String.valueOf(angazman.getTrajanjeNastupa()));
    }
