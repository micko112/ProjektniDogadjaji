    private void jButtonSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSacuvajActionPerformed
        try {
            Izvodjac izvodjac = (Izvodjac) jComboBoxIzvodjac.getSelectedItem();
            if (izvodjac == null) {
                throw new Exception("Morate izabrati izvođača!");
            }
            int trajanje = Integer.parseInt(jTextFieldTrajanje.getText().trim());
            if (trajanje <= 0) {
                throw new Exception("Trajanje nastupa mora biti pozitivan broj!");
            }
            
            if (this.angazman == null) { // Ako je dodavanje
                this.angazman = new Angazman(null, izvodjac, trajanje);
            } else { // Ako je izmena
                this.angazman.setIzvodjac(izvodjac);
                this.angazman.setTrajanjeNastupa(trajanje);
            }
            
            this.uspeh = true;
            this.dispose();
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Trajanje mora biti broj!", "Greška", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greška", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonSacuvajActionPerformed

