    private void postaviStatus(String poruka) {
        jLabelStatus.setText(poruka);
        // Timer koji briše poruku nakon 5 sekundi

        Timer timer = new Timer(5000, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                jLabelStatus.setText(" ");
            }
        });
        timer.setRepeats(false);  // Izvršava se samo jednom
        timer.start();
    }
