    private void jButtonDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDodajActionPerformed

        String ime = jTextFieldIme.getText();
        String prezime = jTextFieldPrezime.getText();
        int brojTelefona = Integer.parseInt(jTextFieldTelefon.getText());

        if (jButtonDodaj.getText().equalsIgnoreCase("dodaj")) {

            Gost gost = new Gost(ime, prezime, brojTelefona);
            controller.dodajGostaUBazu(gost);
            postaviStatus("Gost: " + ime + " uspešno dodat.");
            jTextFieldIme.setText("");
            jTextFieldPrezime.setText("");
            jTextFieldTelefon.setText("");
        } else {
            int id = Integer.parseInt(jTextFieldID.getText());
            Gost gost = new Gost(id, ime, prezime, brojTelefona);

            controller.IzmeniGostaUBazi(gost);
            postaviStatus("Gost: " + ime + " uspešno izmenjen.");
        }
    }//GEN-LAST:event_jButtonDodajActionPerformed

