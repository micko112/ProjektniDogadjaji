    private void jButtonIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIzmeniActionPerformed
        int selektovanRed =jTable.getSelectedRow();
        if(selektovanRed == -1){
            JOptionPane.showMessageDialog(this, "nema selektovanog reda", "greska", JOptionPane.ERROR_MESSAGE);
        }
        else{
        Lokacija lokacijaZaIzmenu = controller.UcitajLokacijeIzBaze().get(selektovanRed);
        
        new KreirajLokaciju(this, true,lokacijaZaIzmenu).setVisible(true);
        
        }
    }//GEN-LAST:event_jButtonIzmeniActionPerformed

    private void jButtonObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonObrisiActionPerformed
            int selektovanaLokacija = jTable.getSelectedRow();
            if(selektovanaLokacija == -1){
                 JOptionPane.showMessageDialog(this, "nema selektovanog reda", "greska", JOptionPane.ERROR_MESSAGE);
            }
            else{
                Lokacija lok = controller.UcitajLokacijeIzBaze().get(selektovanaLokacija);
                controller.obrisiLokaciju(lok);
                osveziTabelu();
            }
    }//GEN-LAST:event_jButtonObrisiActionPerformed

    private void jButtonPretragaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPretragaActionPerformed
        String naziv = jTextFieldNaziv.getText();
        String adresa = jTextFieldAdresa.getText();
    
        
        List<Lokacija> filtriranaLokacija;
        filtriranaLokacija = controller.pretragaLista(naziv, adresa);
        TableModel tm = new TableModel(filtriranaLokacija);
        jTable.setModel(tm);
        
    }//GEN-LAST:event_jButtonPretragaActionPerformed

